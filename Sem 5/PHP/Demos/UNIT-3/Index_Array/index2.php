
<?php
// One way to create an indexed array 
$name_one = array("BCA", "MCA", "PGDCA", "MSCIT", "BCOM"); 
  
// Accessing the elements directly 
echo "Accessing the 1st array elements directly:\n"; 
echo $name_one[2], "\n"; 
echo $name_one[0], "\n"; 
echo $name_one[4], "\n"; 
  
// Second way to create an indexed array 
$name_two[0] = "BCA"; 
$name_two[1] = "MCA"; 
$name_two[2] = "PGDCA"; 
$name_two[3] = "MSCIT"; 
$name_two[4] = "BCOMV"; 
  
// Accessing the elements directly 
echo "Accessing the 2nd array elements directly:\n"; 
echo $name_two[2], "\n"; 
echo $name_two[0], "\n"; 
echo $name_two[4], "\n"; 
  
?> 
